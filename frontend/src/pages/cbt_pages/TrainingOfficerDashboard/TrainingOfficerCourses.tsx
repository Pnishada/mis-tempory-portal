// Add Course Modal Component
interface AddCourseModalProps {
  isOpen: boolean;
  onClose: () => void;
  onSave: (courseData: Partial<CourseType>) => Promise<void>;
  districts: string[];
  instructors: any[];
  centers: Center[];
  categories: string[];
  durations: string[];
  submitting: boolean;
  userDistrict?: string;
  instructorsError?: boolean;
  existingCourses: CourseType[];
}

const AddCourseModal: React.FC<AddCourseModalProps> = ({
  isOpen,
  onClose,
  onSave,
  districts,
  instructors,
  centers,
  categories,
  durations,
  submitting,
  userDistrict,
  instructorsError,
  existingCourses
}) => {
  const [formData, setFormData] = useState({
    name: '',
    code: '',
    category: '',
    duration: '',
    description: '',
    district: userDistrict || '',
    center: '',
    instructor: '',
    students: '0'
  });

  const [isAutoGenerated, setIsAutoGenerated] = useState(true);
  const [officialCourseCodes, setOfficialCourseCodes] = useState<CourseCodeType[]>([]);

  // Load course codes on mount
  useEffect(() => {
    const loadCodes = async () => {
      try {
        const cCodes = await fetchAvailableCourseCodes();
        setOfficialCourseCodes(cCodes);
      } catch (error) {
        console.error('Error loading codes:', error);
      }
    };
    if (isOpen) {
      loadCodes();
    }
  }, [isOpen]);

  // Filter instructors and centers by selected district
  // Filter instructors and centers by selected district
  // Filter instructors and centers by selected district
  const filteredInstructors = Array.isArray(instructors) ? instructors.filter(instructor => {
    if (!instructor.district) return false;
    if (!formData.district) return false;


    const normalize = (s: string) => s.toLowerCase().replace("district", "").trim();
    const d1 = normalize(instructor.district || "");
    const d2 = normalize(formData.district || "");

    // DEBUG LOG
    console.log(`Filtering Instructor: ${instructor.first_name} (${d1}) vs Selected (${d2}) -> Match: ${d1 === d2 || d1.includes(d2) || d2.includes(d1)}`);

    // Check for match after normalization
    return d1 === d2 || d1.includes(d2) || d2.includes(d1);
  }) : [];

  const filteredCenters = Array.isArray(centers) ? centers.filter(center =>
    center.district === formData.district
  ) : [];

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();

    // Validate required fields
    if (!formData.name.trim() || !formData.code.trim() || !formData.district.trim() || !formData.center.trim()) {
      toast.error('Please fill in all required fields (Name, Code, District, and Center)');
      return;
    }

    try {
      const courseToSave = {
        name: formData.name,
        code: formData.code,
        category: formData.category,
        duration: formData.duration,
        description: formData.description,
        district: formData.district,
        center: parseInt(formData.center),
        instructor: formData.instructor ? parseInt(formData.instructor) : null,
        students: parseInt(formData.students) || 0,
        progress: 0,
        status: 'Pending' as const,
        priority: 'Medium' as const,
      };
      await onSave(courseToSave);
      setFormData({
        name: '',
        code: '',
        category: '',
        duration: '',
        description: '',
        district: userDistrict || '',
        center: '',
        instructor: '',
        students: '0'
      });
      onClose();
    } catch (error) {
      console.error('Error saving course:', error);
    }
  };

  // Reset form when modal closes and auto-fill district
  useEffect(() => {
    if (isOpen) {
      setFormData(prev => ({
        ...prev,
        district: userDistrict || prev.district,
        center: '',
        instructor: ''
      }));
    } else {
      setFormData({
        name: '',
        code: '',
        category: '',
        duration: '',
        description: '',
        district: userDistrict || '',
        center: '',
        instructor: '',
        students: '0'
      });
    }
  }, [isOpen, userDistrict]);

  // Reset instructor and center selection when district changes
  useEffect(() => {
    if (isOpen) {
      setFormData(prev => ({
        ...prev,
        center: '',
        instructor: ''
      }));
    }
  }, [formData.district, isOpen]);

  // Auto-generate course code on open or when dependencies change
  useEffect(() => {
    if (isOpen && isAutoGenerated) {
      // Generate based on Name (e.g., 'Web Programming' -> 'WP')
      if (formData.name) {
        generateId();
      }
    }
  }, [isOpen, formData.name, isAutoGenerated, officialCourseCodes, existingCourses]);

  const generateId = () => {
    // 1. Get Course Name Acronym (e.g., 'WP' for 'Web Programming')
    let namePrefix = 'CRS';
    const cleanName = formData.name.trim();

    if (cleanName) {
      // Try to find official code first
      const foundCode = officialCourseCodes.find(c => c.course_name.toLowerCase() === cleanName.toLowerCase());
      if (foundCode) {
        namePrefix = foundCode.course_code;
      } else {
        // Generate Acronym: "Web Programming" -> "WP"
        // remove common stop words for better acronyms
        const words = cleanName.split(/\s+/).filter(w => w.length > 0 && !['and', 'of', 'in', 'for', 'the', '&'].includes(w.toLowerCase()));
        if (words.length > 0) {
          if (words.length === 1) {
            namePrefix = words[0].substring(0, 3).toUpperCase();
          } else {
            namePrefix = words.map(w => w[0]).join('').substring(0, 4).toUpperCase();
          }
        }
      }
    } else if (formData.category) {
      // Fallback to Category
      namePrefix = formData.category.substring(0, 3).toUpperCase();
    }

    // 2. Find next sequence number
    // Format: WP001 (Prefix + 3 digits)
    const prefix = namePrefix;
    let maxNum = 0;

    // Check existing courses for codes starting with this prefix followed by digits
    existingCourses.forEach(course => {
      if (course.code && course.code.startsWith(prefix)) {
        // user might have formats like WP001 or WP-001, handled flexibly?
        // User requested "WP001", so let's stick to that strict format for generation
        // But for parsing, we extract the numbers from the end
        const match = course.code.match(/(\d+)$/);
        if (match) {
          const numPart = parseInt(match[1]);
          if (!isNaN(numPart) && numPart > maxNum) {
            maxNum = numPart;
          }
        }
      }
    });

    const nextNum = (maxNum + 1).toString().padStart(3, '0');
    setFormData(prev => ({ ...prev, code: `${prefix}${nextNum}` }));
    setIsAutoGenerated(true);
  };

  const handleManualCodeChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    setFormData({ ...formData, code: e.target.value.toUpperCase() });
    setIsAutoGenerated(false);
  };

  if (!isOpen) return null;

  return (
    <div className="fixed inset-0 z-50 flex items-center justify-center p-4 bg-black bg-opacity-50">
      <div className="bg-white rounded-xl shadow-2xl w-full max-w-lg sm:max-w-3xl p-5 sm:p-8 relative max-h-[90vh] overflow-y-auto">
        <button
          onClick={onClose}
          className="absolute top-3 sm:top-5 right-3 sm:right-5 text-gray-400 hover:text-gray-600"
        >
          <X className="w-5 sm:w-6 h-5 sm:h-6" />
        </button>

        <h2 className="text-xl sm:text-2xl font-bold text-gray-900 mb-6 sm:mb-8">Add New Course</h2>

        <form onSubmit={handleSubmit} className="grid grid-cols-1 sm:grid-cols-2 gap-5 sm:gap-6">
          <div className="sm:col-span-2">
            <label className="block text-sm font-medium text-gray-700 mb-1">
              Course Name <span className="text-red-500">*</span>
            </label>
            <input
              type="text"
              required
              value={formData.name}
              onChange={(e) => setFormData({ ...formData, name: e.target.value })}
              className="w-full px-3 sm:px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-green-500 focus:border-transparent"
              placeholder="Web Development Fundamentals"
            />
          </div>

          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">
              Course Code <span className="text-red-500">*</span>
            </label>

            <div className="flex gap-2">
              <input
                type="text"
                required
                value={formData.code}
                onChange={handleManualCodeChange}
                className={`w-full px-3 sm:px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-green-500 focus:border-transparent ${isAutoGenerated ? 'bg-gray-50' : 'bg-white'}`}
                placeholder="WP001"
              />
              <button
                type="button"
                onClick={generateId}
                className="p-2 border border-gray-300 rounded-lg hover:bg-gray-50 text-gray-600"
                title="Generate New Code"
              >
                <RefreshCw className="w-5 h-5" />
              </button>
            </div>
          </div>

          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">
              Category
            </label>
            <select
              value={formData.category}
              onChange={(e) => setFormData({ ...formData, category: e.target.value })}
              className="w-full px-3 sm:px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-green-500 focus:border-transparent"
            >
              <option value="">Select Category</option>
              {categories.map((category) => (
                <option key={category} value={category}>{category}</option>
              ))}
            </select>
          </div>

          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">
              Duration
            </label>
            <select
              value={formData.duration}
              onChange={(e) => setFormData({ ...formData, duration: e.target.value })}
              className="w-full px-3 sm:px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-green-500 focus:border-transparent"
            >
              <option value="">Select Duration</option>
              {durations.map((duration) => (
                <option key={duration} value={duration}>{duration}</option>
              ))}
            </select>
          </div>

          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">
              <Users className="w-3 sm:w-4 h-3 sm:h-4 inline mr-1" />
              Student Count
            </label>
            <input
              type="number"
              min="0"
              value={formData.students}
              onChange={(e) => setFormData({ ...formData, students: e.target.value })}
              className="w-full px-3 sm:px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-green-500 focus:border-transparent"
              placeholder="0"
            />
          </div>

          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">
              <Building className="w-3 sm:w-4 h-3 sm:h-4 inline mr-1" />
              Center <span className="text-red-500">*</span>
              {formData.district && (
                <span className="text-xs text-gray-500 ml-1">
                  (in {formData.district})
                </span>
              )}
            </label>
            <div className="relative">
              <Building className="absolute left-3 top-1/2 transform -translate-y-1/2 text-gray-400 w-3 sm:w-4 h-3 sm:h-4" />
              <select
                required
                value={formData.center}
                onChange={(e) => setFormData({ ...formData, center: e.target.value })}
                className="w-full pl-8 sm:pl-10 pr-3 sm:pr-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-green-500 focus:border-transparent appearance-none"
                disabled={!formData.district}
              >
                <option value="">
                  {formData.district ? 'Select Center' : 'Select district first'}
                </option>
                {filteredCenters.map((center) => (
                  <option key={center.id} value={center.id}>
                    {center.name}
                    {center.location && ` - ${center.location}`}
                  </option>
                ))}
              </select>
              <div className="pointer-events-none absolute inset-y-0 right-0 flex items-center px-2 text-gray-700">
                <svg className="fill-current h-3 w-3 sm:h-4 sm:w-4" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 20 20">
                  <path d="M9.293 12.95l.707.707L15.657 8l-1.414-1.414L10 10.828 5.757 6.586 4.343 8z" />
                </svg>
              </div>
            </div>
            {formData.district && filteredCenters.length === 0 && (
              <p className="text-xs text-yellow-600 mt-1">
                No centers found in {formData.district} district
              </p>
            )}
          </div>

          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">
              <User className="w-3 sm:w-4 h-3 sm:h-4 inline mr-1" />
              Instructor {formData.district && (
                <span className="text-xs text-gray-500 ml-1">
                  (in {formData.district})
                </span>
              )}
            </label>
            <div className="relative">
              <User className="absolute left-3 top-1/2 transform -translate-y-1/2 text-gray-400 w-3 sm:w-4 h-3 sm:h-4" />
              <select
                value={formData.instructor}
                onChange={(e) => setFormData({ ...formData, instructor: e.target.value })}
                className="w-full pl-8 sm:pl-10 pr-3 sm:pr-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-green-500 focus:border-transparent appearance-none"
                disabled={!formData.district || instructorsError}
              >
                <option value="">
                  {instructorsError ? 'Unable to load instructors' :
                    formData.district ? 'Select Instructor' : 'Select district first'}
                </option>
                {filteredInstructors.map((instructor) => (
                  <option key={instructor.id} value={instructor.id}>
                    {instructor.first_name} {instructor.last_name}
                    {instructor.email && ` (${instructor.email})`}
                  </option>
                ))}
              </select>
              <div className="pointer-events-none absolute inset-y-0 right-0 flex items-center px-2 text-gray-700">
                <svg className="fill-current h-3 w-3 sm:h-4 sm:w-4" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 20 20">
                  <path d="M9.293 12.95l.707.707L15.657 8l-1.414-1.414L10 10.828 5.757 6.586 4.343 8z" />
                </svg>
              </div>
            </div>
            {instructorsError && (
              <p className="text-xs text-red-600 mt-1 flex items-center">
                <AlertCircle className="w-3 sm:w-4 h-3 sm:h-4 mr-1" />
                Failed to load instructors. Please try again.
              </p>
            )}
            {formData.district && filteredInstructors.length === 0 && !instructorsError && (
              <p className="text-xs text-yellow-600 mt-1">
                No instructors found in {formData.district} district
              </p>
            )}
          </div>

          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">
              District <span className="text-red-500">*</span>
              {userDistrict && <span className="text-green-600 ml-2">(Auto-filled)</span>}
            </label>
            <div className="relative">
              <MapPin className="absolute left-3 top-1/2 transform -translate-y-1/2 text-gray-400 w-3 sm:w-4 h-3 sm:h-4" />
              <select
                required
                value={formData.district}
                onChange={(e) => setFormData({ ...formData, district: e.target.value })}
                className="w-full pl-8 sm:pl-10 pr-3 sm:pr-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-green-500 focus:border-transparent appearance-none"
                disabled={!!userDistrict}
              >
                <option value="">Select District</option>
                {districts.map((district) => (
                  <option key={district} value={district}>
                    {district}
                  </option>
                ))}
              </select>
              <div className="pointer-events-none absolute inset-y-0 right-0 flex items-center px-2 text-gray-700">
                <svg className="fill-current h-3 w-3 sm:h-4 sm:w-4" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 20 20">
                  <path d="M9.293 12.95l.707.707L15.657 8l-1.414-1.414L10 10.828 5.757 6.586 4.343 8z" />
                </svg>
              </div>
            </div>
            {userDistrict && (
              <p className="text-xs text-green-600 mt-1">
                Your assigned district: <strong>{userDistrict}</strong>
              </p>
            )}
          </div>



          <div className="sm:col-span-2 flex justify-end space-x-3 pt-2 sm:pt-4">
            <button
              type="button"
              onClick={onClose}
              className="px-3 sm:px-5 py-2 border border-gray-300 rounded-lg hover:bg-gray-50 font-medium"
              disabled={submitting}
            >
              Cancel
            </button>
            <button
              type="submit"
              disabled={submitting}
              className="px-3 sm:px-5 py-2 bg-green-600 text-white rounded-lg hover:bg-green-700 font-medium shadow-sm disabled:opacity-70 flex items-center space-x-2"
            >
              {submitting ? (
                <>
                  <Loader2 className="w-3 sm:w-4 h-3 sm:h-4 animate-spin" />
                  <span>Saving...</span>
                </>
              ) : (
                <span>Save Course</span>
              )}
            </button>
          </div>
        </form>
      </div>
    </div>
  );
};

