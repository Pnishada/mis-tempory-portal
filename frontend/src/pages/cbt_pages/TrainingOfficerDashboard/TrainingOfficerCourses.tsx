  const userDistrict = localStorage.getItem("user_district") || "";
  const isTrainingOfficer = userRole === 'training_officer';

  useEffect(() => {
    loadCourses();
    loadCenters();
    loadInstructors();
    loadOptions();
  }, []);

  const loadOptions = async () => {
    try {
      const cats = await fetchCourseCategories();
      setCategories(cats);
      const durs = await fetchCourseDurations();
      setDurations(durs);
    } catch (e) {
      console.error("Failed to load options", e);
    }
  };

  const loadCourses = async () => {
    try {
      setLoading(true);
      const coursesData = await fetchCourses();
      if (Array.isArray(coursesData)) {
        setCourses(coursesData);
      } else {
        console.error('Invalid courses data received:', coursesData);
        setCourses([]);
        toast.error('Received invalid data format for courses');
      }
    } catch (error) {
      console.error('Error loading courses:', error);
      toast.error('Failed to load courses');
      setCourses([]);
    } finally {
      setLoading(false);
    }
  };

